package com.example.adapters;

public class newItem {
    int img;
    String author;
    String timeStamp;
    String text;

    public newItem(int img, String author, String timeStamp, String text) {
        this.author = author;
        this.img = img;
        this.text = text;
        this.timeStamp = timeStamp;
    }
}
